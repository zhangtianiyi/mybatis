package entity;

public class Goods {
	private int gid;
    private String gname;
    private String gmade;
    private double gprice;
    private int gbalance;
    private int gstore;
    private int gnum;
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public String getGmade() {
		return gmade;
	}
	public void setGmade(String gmade) {
		this.gmade = gmade;
	}
	public double getGprice() {
		return gprice;
	}
	public void setGprice(double gprice) {
		this.gprice = gprice;
	}
	public int getGbalance() {
		return gbalance;
	}
	public void setGbalance(int gbalance) {
		this.gbalance = gbalance;
	}
	public int getGstore() {
		return gstore;
	}
	public void setGstore(int gstore) {
		this.gstore = gstore;
	}
	public int getGnum() {
		return gnum;
	}
	public void setGnum(int gnum) {
		this.gnum = gnum;
	}
    
    
}
